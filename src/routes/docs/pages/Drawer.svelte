<section>
  <p>The drawer is a simple way to access many of the essential tools without leaving the presentation view.</p>
  <p>
    In the top of the drawer you have all of the tabs, and a search are to quickly find what you need. Then on the left side you have different sections for the selected tab. In
    the middle is all of the contents, and any info or a clock will show on the right.
  </p>
</section>

<section>
  <h4>Shows</h4>
  <p>This is where all your lyrics, event screens, seromns, slide shows and more are located. Click on them to show a preview, or double click to show to output.</p>
</section>

<section>
  <h4>Media</h4>
  <p>Add folders from your device to view your images and videos. Click on them to get a preview or double click to show in output. Can be dragged on slides.</p>
</section>

<section>
  <h4>Overlays</h4>
  <p>Create overlays and click on them to show one or more over the output. Useful for alerts or offering info. Can be dragged on top of slides.</p>
</section>

<section>
  <h4>Templates</h4>
  <p>Create and add slide templates to quickly change the style of your presentations.</p>
</section>

<section>
  <h4>Audio (TBA)</h4>
  <p>Play one or more audio files from your device, with volume controls.</p>
</section>

<section>
  <h4>Scripture</h4>
  <p>Choose between over 100 bible versions powered by bible.api. Easily select verses and create a show.</p>
</section>

<section>
  <h4>Player</h4>
  <p>Play any YouTube videos without leaving FreeShow, with more comming soon.</p>
</section>

<section>
  <h4>Live</h4>
  <p>
    With this you can mirror your screen or just a single window to the output, useful to showcase a website for example. You can also show connected cameras and play audio from
    microphones to the output.
  </p>
</section>
