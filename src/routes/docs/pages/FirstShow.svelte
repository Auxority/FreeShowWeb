<section>
  <p>In FreeShow a show is your slideshow, it can be everything from song lyrics, events, sermon, images, bible verses, and much more!</p>
</section>

<section>
  <h4>Creating a show</h4>
  <p>
    To create a show you have to.press the big "Add show" button, in the bottom center in the drawer. Type a name and choose a category. In the big empty box, you can paste a lyric
    or type something, then press "Create show". If you have pasted a lyric the program will automatically add the necessary groups to the show.
  </p>
</section>

<section>
  <h4>Edit a show</h4>
  <p>
    To edit a show, open the show you want to edit and go to the "Edit" tap in the top section. You can there type directly in the textbox, move them around and resize. Change
    slides on the left side. On the right side there is a section to edit the text in the selected items. There are different tabs to edit the contents of the item, edit the item,
    create new items, and edit the slide.
  </p>
</section>

<section>
  <h4>Items</h4>
  <p>Your slides can have different items, the normal is the text item, you can also add media, audio, timers and svg icons to your slides.</p>
</section>

<section>
  <h4>Backgrounds</h4>
  <p>
    In the show tab, if you drag and drop a media item from the drawer it will be set as a background to the current slide you are on. Meaning if you click on the slide both the
    background and the slide contents will show in the output. The background will stay on until it gets cleared or another background is shown.
  </p>
</section>
