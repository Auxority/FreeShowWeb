<section>
  <p>In the application menu, under the "File" tab, there is an option named "Import".</p>
  <p>Here you have a bunch of different options to import different file types. Most of them will create a "show".</p>
</section>

<section>
  <h4>Project</h4>
  <p>A FreeShow project is a file containing a project and all of it shows, it's useful to easily transfer a project to another computer.</p>
</section>

<section>
  <h4>Importing PowerPoint</h4>
  <p>If you want to import PowerPoint presentations you have two options:</p>
  <ol style="margin-left: 30px;">
    <li>To import just the text, select PowerPoint.</li>
    <li>To import all the styles, you have to export the presentation to PDF inside PowerPoint, or use an online PPTX to PDF converter. And then import the PDF file.</li>
  </ol>
</section>

<section>
  <h4>Importing PDF</h4>
  <p>If you import as PDF, all the pages will be converted to images, placed in the documents/FreeShow/Import folder. And then added to a show.</p>
</section>
