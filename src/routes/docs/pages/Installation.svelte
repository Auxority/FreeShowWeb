<section>
  <h4>Install on Windows</h4>
  <p><strong>exe:</strong> Double-click on the .exe file, then click "More info", then "Run anyway". Then you are good to go.</p>
</section>

<section>
  <h4>Install on Mac OS</h4>
  <p><strong>dmg:</strong> Right-click on the .dmg file, click "Open". When promted click "Open" again. Then you should be good to go.</p>
</section>

<section>
  <h4>Install on Linux</h4>
  <p><strong>snap:</strong> sudo snap install freeshow</p>
  <p>
    <strong>AppImage:</strong> Right-click the .AppImage file, choose "Properties", go to "Permissions", and check the box that says "Allow executing file as program", then double-click
    the file and you should be good to go!
  </p>
</section>
