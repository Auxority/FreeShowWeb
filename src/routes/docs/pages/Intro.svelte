<p>FreeShow is a free and open-source program to show slides and lyrics on a screen. The project was started in late 2021, with the first release in February 2022.</p>

<p>
  The program was created because I couldn't find any free and good softwares to match the expensive ones. I really wanted to make it as easy to understand as possiple. It might
  take some time getting used to, but I believe it will make the process of creating and showing lyrics and media easy and fun.
</p>

<p>FreeShow is availiable on Windows, Mac OS and Linux. It has many good features, and is still in active development, so is should get more amazing and unique features.</p>
