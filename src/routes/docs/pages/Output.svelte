<section>
  <p>
    In the top right corner there is a button to enable the output window, it is red if the window is live. Underneath the button there is a preview of how the output is shown,
    with an audio bar to the right.
  </p>
</section>

<section>
  <h4>Layers</h4>
  <p>
    The output have different layers, with determines what gets priority over what. Backgrounds is shown underneath everything, then slides, overlays, audio, and slide timers. If a
    layer is active you can click on it to see more info, like media controls for the background. If a layer is selected, the button will change to red. If you click on it, it will
    clear that single layer. There is also a big clear all button to clear all layers.
  </p>
</section>

<section>
  <h4>Controls</h4>
  <p>
    Under the output preview area there are some buttons, the first and the last is to go to next or previous show in the current project. Then the arrows are to go to the next /
    previous slide in the current show, you can also use the arrow keys or the space bar to change slide. Then there is a lock button to lock the output, meaning you cant
    accidentally change the output in any way. And lastly there is a play / reset button which will show the selected show or replay the current shown slide.
  </p>
</section>
