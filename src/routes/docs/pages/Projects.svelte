<section>
  <h4>Creating a project</h4>
  <p>
    To create a project click on the folder icon in the top left, at the bottom you will see two buttons, one to create folders and one to create projects. I recommend you organize
    your projects into folders, you can also create folders inside folders. When you create a project you can drag it around if you want to move it. To select it click on it, you
    can also double click to open it, that will also open the first show in your project.
  </p>
</section>

<section>
  <h4>Adding items project</h4>
  <p>
    To add a show or media item or player item, you can just drag and drop it in you project in the top left. And to move them around, just drag and drop them inside the project.
    You can press arrow down or up to navigate in the project.
  </p>
</section>

<section>
  <h4>Tip</h4>
  <p>Press any letter key on your keyboard to search in the drawer, then press enter to quickly add it to the current project.</p>
</section>
