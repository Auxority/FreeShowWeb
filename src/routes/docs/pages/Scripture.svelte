<section>
  <p>Easily find and show bible verses.</p>
</section>

<section>
  <h4>Local file or API</h4>
  <p>
    You can import bibles from a local file. In the popular format Zefania, or a few other formats. Or choose one or more bibles from <a
      href="https://scripture.api.bible/"
      target="_blank">API.Bible</a
    >.
  </p>
</section>

<section>
  <h4>Create a show</h4>
  <p>
    Creating a show will allow you to customize the scripture as you want. Select a vers by clicking on it, select multiple verses by holding Ctrl/Cmd key, or the Shift key to
    select a range. Then change the values in the right drawer menu, and press "Create show".
  </p>
</section>

<section>
  <h4>On the fly</h4>
  <p>
    If you double click a verse, it will show in the output. Or you can type in the drawer serach, with auto complete. For example if you type "Gen", it will auto complete to
    "Genesis", then type 1:1-3, to select chapter 1, verse 1-3. You can even select more verses by adding "+", e.g. "Genesis 1:1-2+5". When you have selected the verses you can
    press Enter to show it in the output.
  </p>
</section>
