<section>
  <p>Showing text on a screen should be easy to read and follow along and look be pretty.</p>
  <p>When showing text, there are many factors to count for, like how big the screen is, and who are the audience.</p>
</section>

<section>
  <h4>Let's start with the basics</h4>
  <p>
    Your text should should be clear, meaning you should center the text and remove unwanted punctuations like dots and exclamation marks. You should also try to keep a maximum of
    5 lines. Meaning if your verse is longer that that tou should divide it.
  </p>
</section>

<section>
  <h4>Backgrounds are key</h4>
  <p>Use matching backgrounds, preferable with suttle movements, but don't let the background distract from the main show.</p>
</section>

<section>
  <h4>Customize for your audience</h4>
  <p>
    If you have an old audience you should keep a high contrast between the text and the background. A good tip is to add a black background behind just the text, which means you
    can have any background, and an easy to read text.
  </p>
</section>

<section>
  <h4>Font</h4>
  <p>
    The font you use could be day/night difference, the letters should not be clumped together and you can use bold text to make it easier to read. I recommend CMG Sans, or Tahoma.
  </p>
</section>

<section>
  <h4>Simple</h4>
  <p>
    I have noticed people using "(x4)" at the end of some slides, which would seem like a simple way. But it's bad, because both you and the audience "have to count" which could be
    distracting, and there is a possibility you could miscount, or the numbers could be wrong. It's much better to repeat the same slide in your layout, which is easy to do in
    FreeShow. Meaning you always know where you are in the lyrics.
  </p>
</section>

<section>
  <h4>Be early</h4>
  <p>
    It's much better to switch slides one or two words before the next line, because people read and remember it unconsciously, and they then have time to read the first word on
    the new slide.
  </p>
</section>

<section>
  <h4>Breaks</h4>
  <p>
    If there is a long break, or outro in a song you should remove the text and keep only the background. Of course you will have to remember how long the break lasts, you can use
    notes to help. Because if you can do that, the audience gets a much nicer experience, and they can know that they are supposed to sing at that moment.
  </p>
</section>
